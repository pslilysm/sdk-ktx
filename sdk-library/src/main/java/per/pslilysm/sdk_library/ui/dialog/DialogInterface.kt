package per.pslilysm.sdk_library.ui.dialog

/**
 * Interface that defines a dialog-type class that can be shown, dismissed.
 *
 * @author pslilysm
 * Created on 2023/06/29 14:42
 * @since 2.2.0
 */
interface DialogInterface {

    /**
     * show the dialog
     */
    fun show()

    /**
     * dismiss the dialog
     */
    fun dismiss()

}